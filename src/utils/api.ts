// Адрес твоего сервера. 
// Если тестируешь в браузере — localhost, если на реальном Android — нужен IP компьютера.
const SERVER_URL = 'https://serverapp-f0wj.onrender.com';

export const sendTestUser = async (name: string) => {
  try {
    // fetch — это стандартная команда "сходить в интернет по адресу"
    await fetch(`${SERVER_URL}/api/test-user`, {
      method: 'POST', // Мы "отправляем" (POST), а не "запрашиваем" (GET)
      headers: {
        'Content-Type': 'application/json', // Говорим серверу: "Внутри будет JSON-текст"
      },
      // Превращаем объект { username: "имя" } в строку, чтобы она пролезла через сеть
      body: JSON.stringify({ username: name }),
    });

    console.log("Готово! Имя улетело на сервер:", name);
  } catch (error) {
    // Если интернет пропал или ссылка битая, увидим ошибку здесь
    console.log("Ошибка отправки:", error);
  }
};

